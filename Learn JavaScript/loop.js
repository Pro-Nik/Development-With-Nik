// First 10 numbers sum
// for loop

let sum = 0;
for (let i = 0; i <=10; i++) {
    sum +=i;
}
console.log(sum);


// While loop
let i =0;
let sum1 =0;
while (i<=10) {
    sum1 +=i;
    i++;
}
console.log(sum1)

// do-while atleast one time run always

let sum3=0;

do {
    sum3 +=i;
    i++;
} while (i<=10);
console.log(sum3)
